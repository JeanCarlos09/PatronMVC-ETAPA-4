// Modelo: representa los datos y la lógica de negocio
public class Usuario
{
    // Propiedad para almacenar el nombre del usuario.
    public string Nombre { get; set; }

    // Propiedad para almacenar la edad del usuario.
    public int Edad { get; set; }

    // Propiedad para almacenar la cédula del usuario.
    public string Cedula { get; set; }

    // Propiedad para almacenar el número de celular del us
    public string Celular { get; set; }

    // Propiedad para almacenar el correo electrónico del usuario.
    public string Correo { get; set; }

    // Propiedad para almacenar la contraseña del usuario.
    public string Contrasena { get; set; }
}
